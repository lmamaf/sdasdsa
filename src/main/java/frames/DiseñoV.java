    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nombreUsuario = txt_usuario.getText();
    String contrasena = new String(txt_contrasena.getPassword()); // Convertir la contraseña a String
    
    // Obtener la conexión a la base de datos utilizando la clase DataBase
    Connection connection = DataBase.conectar();
    
    if (connection != null) { // Verificar si se estableció la conexión correctamente
        // Verificar el inicio de sesión utilizando el servicio de inicio de sesión
        LoginService loginService = new LoginService(connection);
        
        // Obtener el cargo del empleado
        SesionService sesionService = new SesionService(connection);
        String cargoEmpleado = sesionService.verificarInicioSesionYObtenerCargo(nombreUsuario, contrasena);
        
        if (cargoEmpleado != null) {
            JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso");
            abrirFrameSegunCargo(cargoEmpleado, loginService.obtenerIDEmpleado(nombreUsuario, contrasena),connection);
        } else {
            JOptionPane.showMessageDialog(this, "Credenciales incorrectas");
            // Si las credenciales son incorrectas, se considera un intento fallido
            // Aquí podrías realizar alguna acción adicional si lo deseas, como registrar el intento fallido en alguna tabla de la base de datos
        }
        
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo establecer la conexión a la base de datos");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

