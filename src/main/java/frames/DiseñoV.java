    private void abrirFrameSegunCargo(String cargoEmpleado, int idEmpleado, Connection connection) {
    if ("admin".equalsIgnoreCase(cargoEmpleado)) {
        // Abre el frame correspondiente al cargo de administrador
        AdmiV admiV = new AdmiV(idEmpleado, connection);
        admiV.setVisible(true);
        this.dispose(); // Cierra el frame actual
    } else {
        // Abre el frame correspondiente al cargo de usuario normal
        MenuV menuV = new MenuV(idEmpleado, connection);
        menuV.setVisible(true);
        this.dispose(); // Cierra el frame actual
    }
}

